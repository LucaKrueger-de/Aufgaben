package com.btcag.bootcamp.Aufgaben1;

import java.util.Scanner;

public class ArithmeticOperation {
    public static void main(String[] args) {
        Scanner scanner = new Scanner (System.in);
        int var = 3;
        System.out.print(var);
        System.out.print(++var); //pre-Inkrement. Erst wird um 1 erhöht, dann der erhöhte Wert verwendet;
        System.out.print(var++); //post-Inkrement. Erst wird die Var. genutzt, danach um 1 erhöht




    }
}
